package variable;

public class Var3 {

    public static void main(String[] args) {

        // 정수
        byte a = 127; // - 128 ~ 127
        short b = 32767; // - 32768 ~ 32767
        int c = 2147483647; // - 2147483648 ~ 2147483647
        long d = 10L; // 8byte

        // 실수
        float f = 10.0f;
        double g = 10.0;
    }
}
